QUERY = "nickel production"
COLLECTION = "try_chroma_smoke"


def _step(label: str) -> float:
    print(f"[{time.strftime('%H:%M:%S')}] {label} ...", flush=True)
    return time.perf_counter()


def _done(label: str, started: float) -> None:
    elapsed = time.perf_counter() - started
    print(f"[{time.strftime('%H:%M:%S')}] {label} OK ({elapsed:.2f}s)", flush=True)


def main() -> int:
    parser = argparse.ArgumentParser(description="Minimal Chroma index + retrieve smoke test")
    parser.add_argument(
        "--persist",
        type=Path,
        default=Path("./data/try_chroma"),
        help="Directory for Chroma persistence (default: ./data/try_chroma)",
    )
    args = parser.parse_args()
    persist = args.persist.resolve()
    persist.mkdir(parents=True, exist_ok=True)
    model_dir = (
        Path(__file__).resolve().parent / "assets/models/chroma/onnx_models/all-MiniLM-L6-v2"
    )
    os.environ.setdefault("ADVANCE_RAG_ONNX_MODEL_DIR", str(model_dir.resolve()))

    print("Chroma minimal smoke test")
    print(f"  persist_dir : {persist}")
    print(f"  collection  : {COLLECTION}")
    print("  embedding   : default cpu_local (all-MiniLM-L6-v2 via chromadb ONNX)")
    print(f"  document    : {DOC_TEXT!r}")
    print(f"  query       : {QUERY!r}")
    print(flush=True)

    t0 = _step("Create PersistentClient")
    client = chromadb.PersistentClient(path=str(persist))
    _done("Create PersistentClient", t0)

    t1 = _step("Get or create collection (may download embedding model on first run)")
    collection = client.get_or_create_collection(
        name=COLLECTION,
        metadata={"hnsw:space": "cosine"},
    )
    _done("Get or create collection", t1)

    t2 = _step("Upsert one document")
    collection.upsert(
        ids=[DOC_ID],
        documents=[DOC_TEXT],
        metadatas=[{"source": "try_chroma.py"}],
    )
    _done("Upsert one document", t2)

    count = collection.count()
    print(f"  document_count: {count}", flush=True)

    t3 = _step("Dense query (embeds query text, then searches)")
    result = collection.query(query_texts=[QUERY], n_results=1)
    _done("Dense query", t3)

    ids = (result.get("ids") or [[]])[0]
    docs = (result.get("documents") or [[]])[0]
    dists = (result.get("distances") or [[]])[0]

    if not ids:
        print("FAIL: query returned no results", file=sys.stderr)
        return 1

    print("\nQuery result:")
    print(f"  id       : {ids[0]}")
    print(f"  document : {docs[0] if docs else ''}")
    print(f"  distance : {dists[0] if dists else 'n/a'}")
    print("\nSmoke test passed.", flush=True)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
